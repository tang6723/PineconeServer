package com.ft.db.mapping;

/**
 *
 * @author Tang
 */
public class MerchantRole {
    private String merchantRoleID ;
    private String merchantID ;
    private int serialNo ;
    private String roleCode ;
    private String roleDesc ;
    
    private String enableFlag ;
    private String deleteFlag ;
    private String remarks ;

    public String getMerchantRoleID() {
        return merchantRoleID;
    }

    public void setMerchantRoleID(String merchantRoleID) {
        this.merchantRoleID = merchantRoleID;
    }

    public String getMerchantID() {
        return merchantID;
    }

    public void setMerchantID(String merchantID) {
        this.merchantID = merchantID;
    }

    public int getSerialNo() {
        return serialNo;
    }

    public void setSerialNo(int serialNo) {
        this.serialNo = serialNo;
    }

    public String getRoleCode() {
        return roleCode;
    }

    public void setRoleCode(String roleCode) {
        this.roleCode = roleCode;
    }

    public String getRoleDesc() {
        return roleDesc;
    }

    public void setRoleDesc(String roleDesc) {
        this.roleDesc = roleDesc;
    }

    public String getEnableFlag() {
        return enableFlag;
    }

    public void setEnableFlag(String enableFlag) {
        this.enableFlag = enableFlag;
    }

    public String getDeleteFlag() {
        return deleteFlag;
    }

    public void setDeleteFlag(String deleteFlag) {
        this.deleteFlag = deleteFlag;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
    
    
    
}
